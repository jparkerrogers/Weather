import Foundation
import UIKit

extension UIColor {
    
    static func flatOrangeColor() -> UIColor {
        
        return UIColor(red: 0.976, green: 0.624, blue: 0.220, alpha: 1.00)
    }
    
    static func flatRedColor() -> UIColor {
        
        return UIColor(red: 0.812, green: 0.286, blue: 0.271, alpha: 1.00)
    }
    
    static func flatLightBlueColor() -> UIColor {
        
        return UIColor(red: 0.196, green: 0.514, blue: 0.780, alpha: 1.00)
    }
    
    static func flatLightYelloColor() -> UIColor {
        
        return UIColor(red: 0.976, green: 0.769, blue: 0.212, alpha: 1.00)
    }
}